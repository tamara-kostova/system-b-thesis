from fastapi import APIRouter, Depends
from pydantic import BaseModel
from sqlalchemy import text
from sqlalchemy.orm import Session

from shared.db import get_db

router = APIRouter(prefix="/datasets", tags=["datasets"])


class DatasetSummary(BaseModel):
    id: str
    name: str
    description: str
    population_size: int
    time_range_from: str
    time_range_until: str
    domains: list[str]
    omop_cdm_version: str


@router.get("", response_model=list[DatasetSummary])
def list_datasets(db: Session = Depends(get_db)):
    return [_get_dataset(db)]


@router.get("/{dataset_id}", response_model=DatasetSummary)
def get_dataset(dataset_id: str, db: Session = Depends(get_db)):
    return _get_dataset(db)


def _get_dataset(db: Session) -> DatasetSummary:
    stats = db.execute(text("""
        SELECT
            COUNT(DISTINCT p.person_id)                          AS population_size,
            MIN(vo.visit_start_date)::text                       AS time_from,
            MAX(vo.visit_start_date)::text                       AS time_until
        FROM cdm.person p
        LEFT JOIN cdm.visit_occurrence vo ON vo.person_id = p.person_id
    """)).mappings().one()

    return DatasetSummary(
        id="synthea-omop-v1",
        name="Synthetic OMOP Dataset",
        description=(
            "Synthetic patient data generated by Synthea and mapped to OMOP CDM v5.4. "
            "Contains conditions, medications, and measurements for research purposes."
        ),
        population_size=stats["population_size"],
        time_range_from=stats["time_from"] or "unknown",
        time_range_until=stats["time_until"] or "unknown",
        domains=["Condition", "Drug", "Measurement", "Visit"],
        omop_cdm_version="5.4",
    )
